    override suspend fun load(url: String): LoadResponse {
        val slug = url.trim().trimEnd('/').substringAfterLast("/")
        val res = app.get("$mainUrl/api/film/$slug", headers = apiHeaders, timeout = 20)
            .parsedSafe<NguonCDetailResponse>()
        val movie = res?.movie
            ?: throw ErrorLoadingException("Không thể tải dữ liệu phim")

        // Build episode list
        val epMap = linkedMapOf<String, MutableList<String>>()
        movie.episodes?.forEachIndexed { idx, server ->
            val serverName = server.server_name ?: server.name
                ?: if (idx == 0) "Vietsub" else "Thuyết minh"
            val items = server.items ?: server.list ?: emptyList()
            items.forEach { ep ->
                val embed = ep.embed?.replace("\\/", "/")?.trim() ?: ""
                val m3u8 = ep.m3u8?.replace("\\/", "/")?.trim() ?: ""
                val epName = ep.name ?: "0"
                if (embed.isNotBlank())
                    epMap.getOrPut(epName) { mutableListOf() }.add("$serverName::$embed")
                if (m3u8.isNotBlank() && m3u8 != embed)
                    epMap.getOrPut(epName) { mutableListOf() }.add("$serverName::$m3u8")
            }
        }
        if (epMap.isEmpty()) throw ErrorLoadingException("Không tìm thấy tập phim")

        val episodes = epMap.map { (epName, embeds) ->
            newEpisode(embeds.distinct().joinToString("|")) {
                this.name = "Tập $epName"
                this.episode = epName.toIntOrNull()
            }
        }.sortedBy { it.episode ?: 0 }

        // Metadata
        val categories = movie.category ?: emptyMap()
        val theLoai = categories.values
            .find { it.group?.name == "Thể loại" }
            ?.list?.mapNotNull { it.name } ?: emptyList()
        val quocGia = categories.values
            .find { it.group?.name == "Quốc gia" }
            ?.list?.mapNotNull { it.name }?.joinToString(", ") ?: ""
        val nam = categories.values
            .find { it.group?.name == "Năm" }
            ?.list?.firstOrNull()?.name ?: ""

        val plot = buildString {
            val orig = movie.original_name ?: ""
            if (orig.isNotBlank() && orig != (movie.name ?: ""))
                append("<i>$orig</i><br><br>")
            fun info(l: String, v: String?) { if (!v.isNullOrBlank()) append("<b>$l:</b> $v<br>") }
            info("📺 Trạng thái", movie.current_episode)
            if ((movie.total_episodes ?: 0) > 0) info("🎬 Số tập", "${movie.total_episodes} tập")
            info("⏱ Thời lượng", movie.time)
            info("🎥 Chất lượng", movie.quality)
            info("🔊 Ngôn ngữ", movie.language)
            info("🌍 Quốc gia", quocGia)
            info("🎬 Đạo diễn", movie.director)
            info("🎭 Diễn viên", movie.casts)
            val desc = movie.description ?: ""
            if (desc.isNotBlank()) { append("<br><b>📖 NỘI DUNG</b><br>"); append(desc) }
        }

        return newTvSeriesLoadResponse(movie.name ?: "", url, TvType.TvSeries, episodes) {
            this.posterUrl = movie.poster_url ?: movie.thumb_url
            this.plot = plot
            this.tags = theLoai
            this.year = nam.toIntOrNull()
        }
    }

    // ═══════════════════════════════════════════════════════════
    //  LoadLinks – WebView captures the REAL m3u9 URL
    //
    //  How streamc.xyz works (from DevTools):
    //  1. Browser loads embed.php?hash=XXX
    //  2. Cloudflare sets session cookie
    //  3. JS (player.js) reads data-obf → extracts sUb token
    //  4. JS generates xat auth token from session
    //  5. JW Player fetches: /{sUb_token}.m3u9?xat={auth}
    //  6. Server returns PLAINTEXT m3u9 with .png segments
    //
    //  Strategy: WebView loads embed page → JS runs → intercept
    //  the m3u9 request OR the .png segment requests
    // ═══════════════════════════════════════════════════════════

    override suspend fun loadLinks(
        data: String,
        isCasting: Boolean,
        subtitleCallback: (SubtitleFile) -> Unit,
        callback: (ExtractorLink) -> Unit
    ): Boolean {
        val entries = data.split("|").mapNotNull { entry ->
            val parts = entry.trim().split("::", limit = 2)
            when {
                parts.size == 2 -> Pair(parts[0], parts[1])
                parts.size == 1 && parts[0].startsWith("http") -> Pair("Vietsub", parts[0])
                else -> null
            }
        }

        for ((serverName, embedUrl) in entries) {
            try {
                if (resolveEmbed(serverName, embedUrl, callback)) return true
            } catch (e: Exception) {
                println("[NguonC] Error: ${e.message}")
            }
        }
        return false
    }

    private suspend fun resolveEmbed(
        serverName: String,
        rawUrl: String,
        callback: (ExtractorLink) -> Unit
    ): Boolean {
        var embedUrl = rawUrl

        // Fix legacy phimmoi.net links
        if (rawUrl.contains("sing.phimmoi.net")) {
            val hash = Regex("""/([^/]+)/hls\.m3u8""").find(rawUrl)?.groupValues?.get(1) ?: return false
            embedUrl = "https://embed15.streamc.xyz/embed.php?hash=$hash"
        }

        println("[NguonC] === Resolving: $embedUrl ===")

        // ═══════════════════════════════════════════════════════════
        //  Local Proxy Server approach (proven concept from 6 rounds)
        //
        //  WHY: ExoPlayer uses CronetDataSource which ONLY supports http/https.
        //  - file:// URI → ERR_UNKNOWN_URL_SCHEME
        //  - streamc.xyz m3u8 URL → needs cookie (ExoPlayer can't replay)
        //  - CDN segment URLs → need Referer header (ExoPlayer sends wrong one)
        //
        //  SOLUTION: Local HTTP proxy on 127.0.0.1:
        //  1. Serve m3u8 with segment URLs rewritten to point to proxy
        //  2. Proxy fetches real CDN segments with correct Referer
        //  3. ExoPlayer ↔ localhost (no auth) ↔ CDN (with auth headers)
        //
        //  FLOW:
        //  ExoPlayer → GET http://127.0.0.1:PORT/stream.m3u8
        //           ← m3u8 with segments as /seg?url=ENCODED_CDN_URL
        //  ExoPlayer → GET http://127.0.0.1:PORT/seg?url=ENCODED_CDN_URL
        //           ← proxy fetches CDN URL with Referer → returns bytes
        // ═══════════════════════════════════════════════════════════
        try {
            // Step 1: WebView intercept m3u8 content
            val interceptor = WebViewResolver(
                Regex("""streamc\.xyz/[A-Za-z0-9+/=_-]+\.m3u8""")
            )
            val resp = app.get(
                embedUrl,
                interceptor = interceptor,
                headers = mapOf("Referer" to "$mainUrl/", "User-Agent" to UA),
                timeout = 30
            )
            val m3u8Content = resp.text
            val embedDomain = Regex("""https?://[^/]+""").find(embedUrl)?.value ?: ""

            // Validate: must be real m3u8 with segment URLs
            if (!m3u8Content.contains("#EXTM3U")) {
                println("[NguonC] m3u8 content invalid, trying segment approach")
                // Fallback: try segment intercept directly (already proven to work for 8s)
                return resolveViaSegment(serverName, embedUrl, callback)
            }

            // Step 2: Start local proxy server
            val serverSocket = java.net.ServerSocket(0)
            val port = serverSocket.localPort
            val proxyUrl = "http://127.0.0.1:$port/stream.m3u8"

            // Rewrite m3u8: replace absolute CDN URLs with proxy URLs
            // CRITICAL: proxy URLs must end with .ts extension
            // ExoPlayer HLS parser rejects non-.ts segment extensions (.png → MALFORMED)
            val rewrittenM3u8 = m3u8Content.lines().joinToString("\n") { line ->
                val trimmed = line.trim()
                if (trimmed.startsWith("https://")) {
                    // Rewrite CDN URL to proxy URL with .ts extension
                    "http://127.0.0.1:$port/seg/${URLEncoder.encode(trimmed, "UTF-8")}/segment.ts"
                } else {
                    line
                }
            }

            // Step 3: Run proxy in background thread
            Thread {
                try {
                    serverSocket.soTimeout = 600_000 // 10 min for long videos
                    while (!serverSocket.isClosed) {
                        try {
                            val client = serverSocket.accept()
                            // Handle each request in separate thread
                            Thread {
                                handleProxyRequest(client, rewrittenM3u8, embedDomain)
                            }.apply { isDaemon = true }.start()
                        } catch (_: java.net.SocketTimeoutException) { break }
                          catch (_: java.net.SocketException) { break }
                    }
                } catch (_: Exception) {}
                try { serverSocket.close() } catch (_: Exception) {}
            }.apply { isDaemon = true }.start()

            println("[NguonC] ✓ Proxy server at $proxyUrl")

            callback(newExtractorLink("NguonC", serverName, proxyUrl, ExtractorLinkType.M3U8) {
                this.referer = ""
                this.quality = Qualities.P1080.value
                this.headers = mapOf("User-Agent" to UA)
            })
            return true

        } catch (e: Exception) {
            println("[NguonC] Proxy approach failed: ${e.message}")
        }

        // Fallback: direct segment (plays 8s at least)
        return resolveViaSegment(serverName, embedUrl, callback)

        println("[NguonC] ✗ All strategies failed for: $embedUrl")
        return false
    }

    /** Handle a single proxy request (m3u8 or segment proxy) */
    private fun handleProxyRequest(
        client: java.net.Socket,
        m3u8Content: String,
        segReferer: String
    ) {
        try {
            val input = client.getInputStream().bufferedReader()
            val output = client.getOutputStream()
            val requestLine = input.readLine() ?: return
            // Read remaining headers
            while (input.readLine()?.isNotBlank() == true) { /* skip */ }

            val path = requestLine.split(" ").getOrNull(1) ?: "/"
            val crlf = "\r\n"

            when {
                // Serve m3u8 playlist
                path.contains("stream.m3u8") -> {
                    val body = m3u8Content.toByteArray(Charsets.UTF_8)
                    output.write(("HTTP/1.1 200 OK${crlf}Content-Type: application/vnd.apple.mpegurl${crlf}Content-Length: ${body.size}${crlf}Access-Control-Allow-Origin: *${crlf}Connection: close${crlf}${crlf}").toByteArray())
                    output.write(body)
                }
                // Proxy a CDN segment (URL format: /seg/ENCODED_URL/segment.ts)
                path.contains("/seg/") -> {
                    val encodedUrl = path.removePrefix("/seg/").removeSuffix("/segment.ts")
                    val segUrl = java.net.URLDecoder.decode(encodedUrl, "UTF-8")
                    if (segUrl.startsWith("https://")) {
                        try {
                            val conn = java.net.URL(segUrl).openConnection() as java.net.HttpURLConnection
                            conn.connectTimeout = 15000
                            conn.readTimeout = 30000
                            conn.setRequestProperty("User-Agent", UA)
                            conn.setRequestProperty("Referer", "$segReferer/")
                            conn.setRequestProperty("Origin", segReferer)
                            conn.connect()

                            if (conn.responseCode == 200) {
                                val bytes = conn.inputStream.readBytes()
                                conn.disconnect()
                                output.write(("HTTP/1.1 200 OK${crlf}Content-Type: video/mp2t${crlf}Content-Length: ${bytes.size}${crlf}Access-Control-Allow-Origin: *${crlf}Connection: close${crlf}${crlf}").toByteArray())
                                output.write(bytes)
                            } else {
                                conn.disconnect()
                                output.write("HTTP/1.1 ${conn.responseCode} Error${crlf}Connection: close${crlf}${crlf}".toByteArray())
                            }
                        } catch (_: Exception) {
                            output.write("HTTP/1.1 502 Bad Gateway${crlf}Connection: close${crlf}${crlf}".toByteArray())
                        }
                    } else {
                        output.write("HTTP/1.1 400 Bad Request${crlf}Connection: close${crlf}${crlf}".toByteArray())
                    }
                }
                else -> {
                    output.write("HTTP/1.1 404 Not Found${crlf}Connection: close${crlf}${crlf}".toByteArray())
                }
            }
            output.flush()
            client.close()
        } catch (_: Exception) {
            try { client.close() } catch (_: Exception) {}
        }
    }

    /** Fallback: intercept single segment URL (plays ~8s, proven working) */
    private suspend fun resolveViaSegment(
        serverName: String,
        embedUrl: String,
        callback: (ExtractorLink) -> Unit
    ): Boolean {
        try {
            val interceptor = WebViewResolver(Regex("""streamaaa\d+\.png"""))
            val resp = app.get(
                embedUrl,
                interceptor = interceptor,
                headers = mapOf("Referer" to "$mainUrl/", "User-Agent" to UA),
                timeout = 30
            )
            val capturedUrl = resp.url ?: ""
            if (capturedUrl.contains("streamaaa")) {
                println("[NguonC] ✓ Fallback segment: ${capturedUrl.take(100)}")
                val embedDomain = Regex("""https?://[^/]+""").find(embedUrl)?.value ?: ""
                callback(newExtractorLink("NguonC", serverName, capturedUrl, ExtractorLinkType.VIDEO) {
                    this.referer = "$embedDomain/"
                    this.quality = Qualities.P1080.value
                    this.headers = mapOf("User-Agent" to UA, "Origin" to embedDomain)
                })
                return true
            }
        } catch (e: Exception) {
            println("[NguonC] Fallback segment failed: ${e.message}")
        }
        return false
    }

    // ═══════════════════════════════════════════════════════════
    //  parseApiItem – confirmed JSON structure
    // ═══════════════════════════════════════════════════════════

    private fun parseApiItem(item: NguonCApiItem): SearchResponse? {
        val slug = item.slug ?: return null
        val title = item.name ?: return null
        val href = "$mainUrl/phim/$slug"
        val poster = item.poster_url ?: item.thumb_url
        val currentEp = item.current_episode ?: ""

        val epCount: Int? = when {
            currentEp.equals("FULL", true) -> null
            currentEp.startsWith("Hoàn tất", true) -> null
            else -> Regex("""[Tt]ập\s*(\d+)""").find(currentEp)?.groupValues?.get(1)?.toIntOrNull()
                ?: Regex("""(\d+)\s*/\s*\d+""").find(currentEp)?.groupValues?.get(1)?.toIntOrNull()
        }

        val lang = item.language ?: ""
        val hasSub = lang.contains("Vietsub", true)
        val hasDub = lang.contains("Thuyết Minh", true)
        val dubStatus = when {
            hasSub && hasDub -> EnumSet.of(DubStatus.Subbed, DubStatus.Dubbed)
            hasDub -> EnumSet.of(DubStatus.Dubbed)
            else -> EnumSet.of(DubStatus.Subbed)
        }
        val quality = when (item.quality?.uppercase()) {
            "FHD", "HD" -> SearchQuality.HD
            "CAM" -> SearchQuality.Cam
            "SD" -> SearchQuality.SD
            else -> SearchQuality.HD
        }

        return newAnimeSearchResponse(title, href, TvType.TvSeries) {
            this.posterUrl = poster
            this.quality = quality
            this.dubStatus = dubStatus
            if (epCount != null) {
                if (hasSub) addSub(epCount)
                if (hasDub) addDub(epCount)
                if (!hasSub && !hasDub) addSub(epCount)
            }
        }
    }

