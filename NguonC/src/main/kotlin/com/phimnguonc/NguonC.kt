//  NguonC Plugin – Rebuilt from real DevTools data (2026-06-28)
//
//  Findings from live DevTools capture:
//  • m3u9 (NOT m3u8) is plaintext HLS, NOT AES-GCM encrypted
//  • URL: embedXX.streamc.xyz/{base64_token}.m3u9?xat={auth_hex}
//  • Segments: malaysXX.hlhlhoho2.top/{hash}/streamaaa0000.png
//  • ?xat= token is generated by JS with session cookie
//  • Segments use .png extension (not .ts!)
//  • Without valid xat → "Unauthorized"
//  • WebView with session cookie → gets valid xat → plaintext m3u9
// ═══════════════════════════════════════════════════════════════

@CloudstreamPlugin
