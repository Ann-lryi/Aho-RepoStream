    override suspend fun search(query: String): List<SearchResponse> {
        val encoded = URLEncoder.encode(query, "utf-8")

        // Try search endpoint
        for (path in listOf("api/films/search?keyword=$encoded", "api/films?keyword=$encoded")) {
            try {
                val res = app.get("$mainUrl/$path", headers = apiHeaders, timeout = 15)
                    .parsedSafe<NguonCApiResponse>()
                if (!res?.items.isNullOrEmpty()) {
                    return res!!.items!!.mapNotNull { parseApiItem(it) }
                }
            } catch (_: Exception) {}
        }
        return emptyList()
    }

    // ═══════════════════════════════════════════════════════════
    //  Load – Film detail
    //  Confirmed JSON structure:
    //  {"status":"success","movie":{"episodes":[{"server_name":"Vietsub #1",
    //   "items":[{"name":"1","slug":"tap-1",
    //   "embed":"https://embed13.streamc.xyz/embed.php?hash=..."}]}]}}
